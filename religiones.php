                <h3>Resumen</h3>
                <p>Religión más importante e influyente de Aeberion junto a los Hijos del Profeta. Llegaría al continente poco después de los Días Oscuros traída por los Pueblos Viajeros. Desplazaría al resto de religiones previas y se convertiría en la única religión hasta la aparición del Profeta siglos después.</p>
                <h3>Mito de la creación</h3>
                <p>En el pasado, Aeberion era un lugar sin nombre, yermo y sin vida. Todo cambió cuando dos de los dioses del Cosmos, Olmeo y Canaudis, descubrieron que estaban esperando su primer hijo. Para poder criarlo necesitaban un lugar apropiado, y buscando a lo largo del Cosmos, dieron con un mundo al que bautizaron como Aeberion. Pero este mundo no reunía las condiciones necesarias para la vida, por lo que los dos dioses se vieron obligados a construirlas en el tiempo restante del embarazo. Así, formaron los continentes y las montañas. Crearon sobre ellos lagos y ríos junto a valles y llanuras. Finalmente crearon los bosques la vegetación, y los poblaron con distintas especies animales.
Olmeo y Canaudis comenzaron a vivir en el mundo que acababan de dotar con vida, y poco tiempo después nacería su hijo, que sería la raza de los elfos. Estos heredaron de sus padres los dioses su gran poder mágico y su inmortalidad.
Tiempo después, los dioses engendraron un segundo hijo, que sería la raza de los enanos. Estos heredaron un menor poder mágico que sus hermanos los elfos, pero a cambio, obtenían una gran curiosidad y habilidad inventiva.
Finalmente, llegaría el tercer hijo, la raza de los humanos. Estos heredaron la menor capacidad mágica de los tres, pero su gran capacidad de adaptación compensaría la diferencia respecto a sus hermanos.
Olmeo y Canaudis criaron a sus tres descendientes con pasión y amor, acudiendo raudos a asistirles cuando alguno de ellos tuviese algún tipo de accidente o herida.
Pero antes o después llegaría el día en que tuviesen que regresar al Cosmos, dejando a sus tres hijos, los elfos, los enanos y los humanos, vivir su vida adulta. Viéndolos marchar hacia el cielo en la forma de dos esferas brillantes, la más grande Olmeo y la menor Canaudis, los hijos no podían soportar la idea de perder a sus padres, así que los elfos, como hermano mayor, convencieron a sus hermanos menores que se hiriesen, haciendo así que Olmeo y Canaudis se quedasen al tener que asistirlos. Pero las heridas sanarían al tiempo, y los padres volverían a prepararse para partir hacia el Cosmos. Ante esto, los hijos de los dioses volverían a herirse, evitando que Olmeo y Canaudis se marchasen, y permanecieran por siempre como dos esferas brillantes en el cielo, cuidando de sus hijos.</p>
                <h3>Ética</h3>
                <p>Para los Hijos de los Dioses, todos los seres vivos descienden de los mismos dos padres creadores: Olmeo y Canaudis. Debido a esto, tanto humanos, elfos y enanos se consideran hermanos y deben cuidarse los unos a los otros.
Los Hijos de los Dioses también se caracterizan por una visión enormemente protectora en torno a la figura de la madre y la mujer, por ello tienden a mostrar una alta sobreprotección sobre ellas.</p>
    
